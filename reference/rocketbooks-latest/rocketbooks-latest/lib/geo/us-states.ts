const STATE_CODES = new Set(US_STATES.map((s) => s.code));

/** Validate a 2-letter state code; returns the uppercased code or null. */
export function normalizeStateCode(raw: unknown): string | null {
  if (typeof raw !== 'string') return null;
  const c = raw.trim().toUpperCase();
  return STATE_CODES.has(c) ? c : null;
}

/** Validate an MM-DD string (e.g. "05-01"); returns it normalized or null. */
export function normalizeMonthDay(raw: unknown): string | null {
  if (typeof raw !== 'string') return null;
  const m = /^(\d{1,2})-(\d{1,2})$/.exec(raw.trim());
  if (!m) return null;
  const mm = Number(m[1]);
  const dd = Number(m[2]);
  if (mm < 1 || mm > 12 || dd < 1 || dd > 31) return null;
  return `${String(mm).padStart(2, '0')}-${String(dd).padStart(2, '0')}`;
}
